
public class POJO {

	int id;
	String name;
	
	public POJO(int id,String name){
		this.id = id;
		this.name = name;
	}
	
	protected int getId() {
		return id;
	}
	protected void setId(int id) {
		this.id = id;
	}
	protected String getName() {
		return name;
	}
	protected void setName(String name) {
		this.name = name;
	}
	
	
	
	
	
}
